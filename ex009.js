var d = 1;
do {
    console.log(`A resposta para DO WHILE é ${d}`);
    d++
} while(d <= 5);
console.log();
var e = 1;
for (var e=1; e<=5; e++) {
    console.log(`A resposta para FOR é ${e}`);
}
